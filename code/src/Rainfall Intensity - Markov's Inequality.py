import numpy as np
import matplotlib.pyplot as plt
from scipy.stats import lognorm

# -----------------------------
# Rainfall Intensity Model
# -----------------------------
# Rainfall is non-negative → Markov applicable

mu_log = 1.0        # mean of log(R)
sigma_log = 1.0     # std of log(R) (heavier tail)
scale = np.exp(mu_log)

# Rainfall threshold range (mm/hr)
k = np.linspace(5, 150, 400)

# True exceedance probability from lognormal model
true_prob = 1 - lognorm.cdf(k, s=sigma_log, scale=scale)

# Mean rainfall (needed for Markov bound)
mean_R = lognorm.mean(s=sigma_log, scale=scale)

# Markov inequality bound
markov_bound = mean_R / k

# -----------------------------
# Plot
# -----------------------------
plt.figure(figsize=(8,6))

plt.plot(k, true_prob, label="True Probability  P(R ≥ k)", linewidth=2)
plt.plot(k, markov_bound, '--', label="Markov Bound  E[R]/k", linewidth=2)

plt.yscale("log")   # IMPORTANT for heavy-tail visualization
plt.xlabel("Rainfall Threshold k (mm/hr)")
plt.ylabel("Probability (log scale)")
plt.title("Rainfall Intensity: Lognormal vs Markov’s Inequality")
plt.legend()
plt.grid(True)

plt.show()

print("Mean Rainfall E[R] =", round(mean_R, 3))



