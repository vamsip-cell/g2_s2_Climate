import numpy as np
import matplotlib.pyplot as plt
from scipy.stats import lognorm

# ---- Parameters in dBR domain ----
mu_dB = 0      # mean in dBR (small mean → peak near low rainfall)
sigma_dB = 5   # standard deviation in dBR

# ---- Convert to natural log domain ----
mu_ln = (mu_dB * np.log(10)) / 10
sigma_ln = (sigma_dB * np.log(10)) / 10

# ---- Rainfall intensity range ----
R = np.linspace(0.01, 50, 1000)

# ---- Lognormal PDF and CDF ----
pdf_R = lognorm.pdf(R, s=sigma_ln, scale=np.exp(mu_ln))
cdf_R = lognorm.cdf(R, s=sigma_ln, scale=np.exp(mu_ln))

# ---- Plot ----
plt.figure(figsize=(10,4))

plt.subplot(1,2,1)
plt.plot(R, pdf_R, linewidth=2)
plt.title("PDF of Rainfall Intensity (Log-normal)")
plt.xlabel("Rainfall Intensity (mm/hr)")
plt.ylabel("PDF")
plt.grid(True)

plt.subplot(1,2,2)
plt.plot(R, cdf_R, linewidth=2)
plt.title("CDF of Rainfall Intensity (Log-normal)")
plt.xlabel("Rainfall Intensity (mm/hr)")
plt.ylabel("CDF")
plt.grid(True)

plt.tight_layout()
plt.show()